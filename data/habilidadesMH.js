// Por categoría
const HABILIDADES_POR_CATEGORIA = {
  condicion:         HABILIDADES_MH.filter(h => h.categoria === 'condicion'),
  veneno:            HABILIDADES_MH.filter(h => h.categoria === 'veneno'),
  enfermedad:        HABILIDADES_MH.filter(h => h.categoria === 'enfermedad'),
  habilidad_especial:HABILIDADES_MH.filter(h => h.categoria === 'habilidad_especial'),
  mecanica:          HABILIDADES_MH.filter(h => h.categoria === 'mecanica'),
};

// Por subtipo
const HABILIDADES_POR_SUBTIPO = HABILIDADES_MH.reduce((acc, h) => {
  if (!acc[h.subtipo]) acc[h.subtipo] = [];
  acc[h.subtipo].push(h);
  return acc;
}, {});

// Búsqueda por nombre (EN o ES)
function getHabilidad(nombre) {
  const nl = nombre.toLowerCase();
  return HABILIDADES_MH.find(h =>
    h.nombre.toLowerCase().includes(nl) ||
    h.nombreEN.toLowerCase().includes(nl)
  ) || null;
}

// Búsqueda por monstruo
function getHabilidadesMonstruo(nombreMonstruo) {
  const nl = nombreMonstruo.toLowerCase();
  return HABILIDADES_MH.filter(h =>
    Array.isArray(h.origen) && h.origen.some(o => o.toLowerCase().includes(nl))
  );
}

module.exports = {
  HABILIDADES_MH,
  HABILIDADES_POR_CATEGORIA,
  HABILIDADES_POR_SUBTIPO,
  getHabilidad,
  getHabilidadesMonstruo,
};
